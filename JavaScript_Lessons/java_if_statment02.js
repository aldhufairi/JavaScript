let result = prompt("what is your result ?")

if( result >= 90)
{
    document.write("Exellent!");

}
else if (result >= 80)
{
    document.write("Very Good");
}
else if (result >= 70)
{
    document.write("Good");
}
else if (result >= 50)
{
    document.write("Pass");
}
else
{
    document.write("Fail");
}


